<!--Préventions-->
<div class="def">
<p class="container">Actions de prévention :</p>

Afin de mieux connaître un volcan et de savoir ce dont il est capable, il est essentiel d’étudier ses éruptions passées, qui sont enregistrées dans les couches géologiques et les dépôts éruptifs qui constituent le volcan lui-même.<br><br>

Ces études permettent aux scientifiques de mieux comprendre son fonctionnement, son histoire, les différents types d’éruptions possibles, et aident ainsi à estimer quels seront les aléas susceptibles de survenir en cas de nouvelle éruption.<br><br>

Lorsque les données existantes sur un volcan en particulier sont insuffisantes, l’étude et l’analyse d’autres volcans du même type (analogues) ailleurs dans le monde est aussi souvent un bon moyen d'améliorer nos connaissances.<br><br>

Face aux phénomènes généralement destructeurs et incontrôlables des éruptions, la seule solution pour préserver les populations est souvent l'évacuation préventive des zones menacées.<br><br>

Cela suppose l’anticipation des phénomènes et nécessite la publication de carte d'aléas (définissant les zones menacées) et de plans d'évacuations, en lien avec une surveillance instrumentale permanente qui détectera les signes précurseurs d’éruption.<br><br>

Les autres actions de prévention concernent :<br><br>

<ul>
    <li>la préparation de plans de secours, d'évacuation, et d’organisation de la vie pendant une éruption, en lien entre services de l’Etat, Collectivités et Citoyens ;</li>
    <li>un aménagement du territoire volontariste, pour limiter l'exposition des populations et des biens, et éviter tout dysfonctionnement majeurs en cas d’éruption de moyenne ou longue durée ;</li>
    <li>une sensibilisation et une information régulière de la population.</li>
</div>


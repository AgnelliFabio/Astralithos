<!--Statistiques-->
<div class="def">
<p class="container">Spécificité du risque volcanique :</p>

Les volcans sont des merveilles naturelles fascinantes et redoutables. Voici quelques statistiques intéressantes :<br><br>

<ul>
    <li>Il existe environ 1 500 volcans actifs à travers le monde.</li>
    <li>Chaque année, entre 50 et 70 volcans entrent en éruption.</li>
    <li>Environ 60% des éruptions volcaniques sont qualifiées d'explosives, avec des projections rapides de cendres, de gaz et de roches volcaniques.</li>
    <li>Les 40% restants sont des éruptions effusives, caractérisées par l'écoulement lent de lave fluide le long des pentes du volcan.</li>
    <li>La majorité des volcans actifs se trouvent le long de l'Anneau de feu du Pacifique.</li>
    <li>Les éruptions volcaniques majeures peuvent avoir un impact sur le climat mondial en réduisant temporairement la température due aux particules et aux gaz rejetés dans l'atmosphère.</li>
    <li>Les éruptions volcaniques peuvent causer des dégâts importants, tels que des coulées de lave, des nuées ardentes, des glissements de terrain et des lahars (coules de boue volcanique).</li>
    <li>Elles peuvent également représenter une menace pour les populations locales en raison des gaz toxiques libérés pendant les éruptions.</li>
</div>


